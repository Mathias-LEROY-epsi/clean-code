package ex1;

public class Calcul {
    /**
     * Effectue l'opération d'addition sur deux nombres entiers.
     *
     * @param a Le premier nombre à ajouter.
     * @param b Le deuxième nombre à ajouter.
     * @return La somme des deux nombres.
     */
    public int additionner(int a, int b) {
        return a + b;
    }

    /**
     * Effectue l'opération de soustraction entre deux nombres entiers.
     *
     * @param a Le nombre à soustraire.
     * @param b Le nombre à soustraire.
     * @return La différence entre les deux nombres.
     */
    public int soustraire(int a, int b) {
        return a - b;
    }
}
